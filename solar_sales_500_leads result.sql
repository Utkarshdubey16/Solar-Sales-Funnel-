    l.region,
    l.product_interested,
    COUNT(*) AS total_won,
    ROUND(AVG(DATEDIFF(c.conversion_date, l.inquiry_date)), 2) AS avg_days_to_convert
FROM Conversions c
JOIN Leads l ON c.lead_id = l.lead_id
WHERE c.status = 'Won'
GROUP BY l.region, l.product_interested
ORDER BY avg_days_to_convert;

