SELECT 
    sc.consultant_name,
    COUNT(c.lead_id) AS total_leads,
    SUM(CASE WHEN c.status = 'Won' THEN 1 ELSE 0 END) AS total_converted,
    ROUND(SUM(CASE WHEN c.status = 'Won' THEN 1 ELSE 0 END) * 100.0 / COUNT(c.lead_id), 2) AS conversion_rate_percent
FROM Conversions c
JOIN Lead_Assignments la ON c.lead_id = la.lead_id
JOIN Solar_Consultants sc ON la.consultant_id = sc.consultant_id
GROUP BY sc.consultant_id
ORDER BY conversion_rate_percent DESC;

SELECT 
    l.source,
    COUNT(c.lead_id) AS total_leads,
    SUM(CASE WHEN c.status = 'Won' THEN 1 ELSE 0 END) AS converted,
    ROUND(SUM(CASE WHEN c.status = 'Won' THEN 1 ELSE 0 END) * 100.0 / COUNT(c.lead_id), 2) AS conversion_rate_percent
FROM Conversions c
JOIN Leads l ON c.lead_id = l.lead_id
GROUP BY l.source
ORDER BY conversion_rate_percent DESC;

SELECT 
    ROUND(AVG(DATEDIFF(c.conversion_date, l.inquiry_date)), 2) AS avg_days_to_convert
FROM Conversions c
JOIN Leads l ON c.lead_id = l.lead_id
WHERE c.status = 'Won';

